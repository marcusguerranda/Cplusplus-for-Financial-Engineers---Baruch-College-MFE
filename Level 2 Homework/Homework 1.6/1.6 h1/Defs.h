#ifndef Defs_H
#define Defs_H

#define PRINT1(a) printf("a is: %1.2f\n", a)  //definition for PRINT1; Variable a gets its value in the function main()
#define PRINT2(a, b) printf("a is: %1.2f, and b is: %1.2f\n", a, b) //definition for PRINT2; Variable a and b gets their value in the function main()

#endif